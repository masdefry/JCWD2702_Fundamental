// COMPARISON OPERATOR
// Operator Pembanding
// Result nya Akan Selalu Boolean (True / False)
// >, >=, <, <=, !=, !==, ==, ===

let num1 = 10
let num2 = 5
console.log(num1 > num2)
console.log(num1 < num2)
console.log(5 >= 5)
console.log(5 <= 5)

// == : Dia Hanya Mengecek Value
console.log(5 == '5')
console.log('abc' == '5')

// === : Mengecek Value & Tipe Data
console.log(5 === '5')
console.log('abc' === '5')

console.log(5 != 5)
console.log('5' != 5)
console.log('5' !== 5)